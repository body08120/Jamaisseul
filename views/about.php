                        <p>L’association <b>JAMAIS SEUL</b> régie par la loi du 1er juillet 1901 a été créée en
                            septembre
                            1978 à l’initiative d’un pasteur sensibilisé à la détresse des femmes victimes de violences
                            conjugales. Un don immobilier a rapidement permis l’ouverture d’un centre d’accueil à REIMS
                            auquel la Préfecture de la Marne accordera le statut de CHRS trois ans plus tard. </p>
                        <p>Comme défini dans les statuts de l’association, le but de celle-ci est de <b>lutter contre
                                toutes
                                formes de précarité morale, physique et matérielle en développant des solutions
                                d’accueil,
                                d’hébergement, d’aide et d’assistance, de soins et d’accompagnement en faveur de toutes
                                personnes en difficultés.</b></p>
                        <p>La charte que l’association a adoptée le 14 octobre 2000, rappelle que « La pauvreté et les
                            précarités, le chômage, la solitude, l’exclusion et de multiples formes de souffrance ne
                            sont
                            pas des fatalités. Ce sont des signes manifestes et douloureux d’un ordre culturel, social
                            et
                            économique qui ne laisse que peu de place aux êtres fragiles et vulnérables. Ces atteintes à
                            la
                            dignité humaine sont en contradiction avec la Déclaration universelle des droits de l’homme.
                            Il
                            est inacceptable qu’un être humain soit enfermé dans sa souffrance ou abandonné dans sa
                            douleur.
                            ». <b>Ces valeurs humanistes</b> sont reprises dans le projet associatif qui institue que
                            les
                            établissements et dispositifs gérés par l’association <b>« sont un réel espace de liberté où
                                l’être souffrant est respecté, accompagné, devenant ainsi acteur de sa propre vie et non
                                considéré comme un objet ou une donnée comptable. »</b>.</p>
                        <p>Afin de répondre aux problématiques et aux besoins d’une partie de la population, JAMAIS SEUL
                            a
                            développé un certain nombre de dispositifs à l’échelle départementale. Aujourd’hui,
                            l’association conçoit et met en œuvre des <b>dispositifs diversifiés et innovants d’aide, de
                                soutien et d’accompagnement auprès de publics vulnérables</b>.</p>
                        <p>Ne pouvant se contenter d’additionner les dispositifs, l’association s’est dotée d’une
                            organisation permettant de fluidifier autant que possible le parcours résidentiel des
                            usagers ;
                            l’efficience de celle-ci devant se traduire par des sorties de l’hébergement vers le
                            logement.</p>

